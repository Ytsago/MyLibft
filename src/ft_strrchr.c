#include "libft.h"

char	*strrchr(const char *s, int c)
{
	size_t	i;

	i = ft_strlen(s);
	while (*(s + i))
	{
		if (c == *(s + i))
			return (s + i);
		i--;
	}
	return ('\0');
}
